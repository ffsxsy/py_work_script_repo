def crc16_modbus(data: bytes) -> int:
    """与 firmware `kit_check_crc16()` 一致。"""
    crc_low = 0xFF
    crc_high = 0xFF
    for byte in data:
        index = crc_low ^ byte
        crc_low = crc_high ^ _CRC_HI[index]
        crc_high = _CRC_LO[index]
    return (crc_high << 8) | crc_low


@dataclass(frozen=True)
class BmsFrame:
    src: int
    src_sub: int
    dest: int
    dest_sub: int
    transport_type: int
    frame_id: int
    cmd_group: int
    cmd_id: int
    payload: bytes

    @property
    def cmd_key(self) -> str:
        return f"0x{self.cmd_group:02X}:0x{self.cmd_id:02X}"


def _pack_version_len(version: int, body_len: int) -> bytes:
    word = ((body_len & 0x7FF) << 5) | (version & 0x1F)
    return struct.pack("<H", word)


def _unpack_version_len(data: bytes | bytearray, offset: int = 1) -> tuple[int, int]:
    word = struct.unpack_from("<H", data, offset)[0]
    return word & 0x1F, (word >> 5) & 0x7FF


def build_frame(
    *,
    src: tuple[int, int],
    dest: tuple[int, int],
    transport_type: int,
    frame_id: int,
    cmd_group: int,
    cmd_id: int,
    payload: bytes = b"",
) -> bytes:
    """按 `createSendFrame()` / `parseCheckRbmsData()` 组 5 字节 LinkMsg 帧。"""
    body = (
        bytes(
            [
                src[0],
                src[1],
                dest[0],
                dest[1],
                transport_type,
                frame_id,
                cmd_group,
                cmd_id,
            ]
        )
        + payload
    )
    body_len = len(body)

    frame = bytearray(LINK_MSG_LEN + body_len)
    frame[0] = PROTOCOL_HEAD
    frame[1:3] = _pack_version_len(PROTOCOL_VERSION, body_len)
    frame[LINK_MSG_LEN : LINK_MSG_LEN + body_len] = body
    crc = crc16_modbus(bytes(frame[LINK_MSG_LEN : LINK_MSG_LEN + body_len]))
    frame[3:5] = struct.pack("<H", crc)
    return bytes(frame)


def parse_check_frame(data: bytes) -> bool:
    """与 firmware `parseCheckRbmsData()` 等价校验。"""
    if len(data) < LINK_MSG_LEN + BODY_HEADER_LEN:
        return False

    version, body_len = _unpack_version_len(data, 1)
    if data[0] != PROTOCOL_HEAD or version != PROTOCOL_VERSION:
        return False
    if body_len != len(data) - LINK_MSG_LEN:
        return False

    crc_expected = struct.unpack_from("<H", data, 3)[0]
    body = data[LINK_MSG_LEN : LINK_MSG_LEN + body_len]
    return crc16_modbus(body) == crc_expected


def try_parse_frames(buffer: bytearray) -> tuple[list[BmsFrame], bytearray]:
    """从 TCP 流中解析一帧或多帧，返回剩余缓存。"""
    frames: list[BmsFrame] = []
    offset = 0

    while offset < len(buffer):
        if buffer[offset] != PROTOCOL_HEAD:
            next_head = buffer.find(PROTOCOL_HEAD, offset + 1)
            if next_head < 0:
                buffer.clear()
                return frames, buffer
            offset = next_head
            continue

        if offset + LINK_MSG_LEN + BODY_HEADER_LEN > len(buffer):
            break

        version, body_len = _unpack_version_len(buffer, offset + 1)
        if version != PROTOCOL_VERSION or body_len < BODY_HEADER_LEN:
            offset += 1
            continue

        frame_len = LINK_MSG_LEN + body_len
        if offset + frame_len > len(buffer):
            break

        chunk = bytes(buffer[offset : offset + frame_len])
        if not parse_check_frame(chunk):
            offset += 1
            continue

        body = chunk[LINK_MSG_LEN : LINK_MSG_LEN + body_len]
        frames.append(
            BmsFrame(
                src=body[0],
                src_sub=body[1],
                dest=body[2],
                dest_sub=body[3],
                transport_type=body[4],
                frame_id=body[5],
                cmd_group=body[6],
                cmd_id=body[7],
                payload=body[BODY_HEADER_LEN:],
            )
        )
        offset += frame_len

    remainder = bytearray(buffer[offset:])
    return frames, remainder


def format_hex(data: bytes) -> str:
    return " ".join(f"{b:02X}" for b in data)
